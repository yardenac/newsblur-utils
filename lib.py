def urlify(name):
    good_chars = 'abcdefghijklmnopqrstuvwxyz1234567890-'
    s = ''
    for char in name:
        char = char.lower()
        if (char in ' _'): s += '-'
        if (char in good_chars): s += char
        else: s += '-'
    return s
